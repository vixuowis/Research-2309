from typing import *
from typing import Tuple


def sum_of_two_numbers(a: int, b: int) -> int:
    """
    This function takes two numbers as input and returns their sum.
    
    Args:
        a (int): The first number.
        b (int): The second number.
    
    Returns:
        int: The sum of the two numbers.
    """
    # Add the two numbers
    sum = a + b
    
    # Return the sum
    return sum
