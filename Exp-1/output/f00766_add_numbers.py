from typing import *
import numpy as np

def add_numbers(a: int, b: int) -> int:
    # This function takes two integers as input and returns their sum.
    #
    # Params:
    # - a (int): The first integer.
    # - b (int): The second integer.
    #
    # Returns:
    # - int: The sum of the two integers.
    
    # Add the two numbers
    sum = a + b
    return sum
