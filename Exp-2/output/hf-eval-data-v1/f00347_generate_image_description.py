from transformers import pipeline


def generate_image_description(input_text):
    """
    This function takes a string as input and returns an image description generated by the 'prompthero/openjourney-v4' model.
    The model is a pre-trained model designed for generating image descriptions.
    
    Parameters:
    input_text (str): The text based on which the image description is to be generated.
    
    Returns:
    str: The generated image description.
    """
    # Load the trained model
    text_to_image = pipeline('text-to-image', model='prompthero/openjourney-v4')
    
    # Use the model to process the input text
    result = text_to_image(input_text)
    
    return result