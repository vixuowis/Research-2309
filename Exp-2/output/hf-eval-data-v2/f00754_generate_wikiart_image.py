# function_import --------------------

from diffusers import DDPMPipeline

# function_code --------------------

def generate_wikiart_image():
    """
    This function generates a new piece of art that resembles WikiArt images using a pre-trained model.

    The model is a diffusion model initialized from 'google/ddpm-bedroom-256' and trained for 5000 steps on the WikiArt dataset.

    Returns:
        image: An image generated by the model.
    """
    pipeline = DDPMPipeline.from_pretrained('johnowhitaker/sd-class-wikiart-from-bedrooms')
    image = pipeline().images[0]
    return image

# test_function_code --------------------

def test_generate_wikiart_image():
    """
    This function tests the 'generate_wikiart_image' function by generating an image and checking its type.
    """
    image = generate_wikiart_image()
    assert isinstance(image, type(image)), 'The function should return an image.'

# call_test_function_code --------------------

test_generate_wikiart_image()