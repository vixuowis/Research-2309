# function_import --------------------

from transformers import pipeline

# function_code --------------------

def generate_motivational_quote(initial_prompt: str) -> str:
    """
    Generate a motivational sports quote using a pre-trained model. 

    Args:
        initial_prompt (str): Initial text to prompt the model.

    Returns:
        str: A motivational sports quote generated by the model.

    Raises:
        ValueError: If the resulting text is empty or None.
    """
    text_generator = pipeline('text-generation', model='TehVenom/PPO_Pygway-V8p4_Dev-6b')
    result = text_generator(initial_prompt, max_length=50)[0]['generated_text']
    if not result:
        raise ValueError('Generated text is empty.')
    return result

# test_function_code --------------------

def test_generate_motivational_quote():
    print("Testing started.")
    
    # Test case 1: Using a generic prompt
    print("Testing case [1/1] started.")
    prompt = 'Motivational quote about sports:'
    quote = generate_motivational_quote(prompt)
    assert quote and type(quote) == str, f"Test case [1/1] failed: Expected a non-empty string, but got {quote}."
    
    print("Testing finished.")

